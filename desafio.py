def listar_contas(contas):
    for conta in contas:
        linha = f"""\
            Agência:\t{conta['agencia']}
            C/C:\t\t{conta['numero_conta']}
            Titular:\t{conta['usuario']['nome']}
        """
        print("=" * 100)
        print(textwrap.dedent(linha))


def main():
    # constantes
    LIMITE_SAQUES = 3
    AGENCIA = "0001"
    # variáveis
    saldo = 0
    limite = 500
    extrato = ""
    numero_saques = 0
    # listas
    usuarios = []
    contas = []

    while True:
        opcao = menu()

        if opcao == "d":
            valor_input = input("Informe o valor do depósito: ")
            valor = validar_valor(valor_input)  # Valida e converte o valor_input

            if valor is not None:
                saldo, extrato = depositar(saldo, valor, extrato)
            else:
                print("\n@@@ Valor inválido! @@@")  # Mensagem se o valor for inválido

        elif opcao == "s":
            valor_input = input("Informe o valor do saque: ")
            valor = validar_valor(valor_input)  # Valida e converte o valor_input

            if valor is not None:
                saldo, extrato, numero_saques = sacar(
                    saldo=saldo,
                    valor=valor,
                    extrato=extrato,
                    limite=limite,
                    numero_saques=numero_saques,
                    limite_saques=LIMITE_SAQUES,
                )
            else:
                print("\n@@@ Valor inválido! @@@")  # Mensagem se o valor for inválido

        elif opcao == "e":
            exibir_extrato(saldo, extrato=extrato)

        elif opcao == "nu":
            criar_usuario(usuarios)

        elif opcao == "nc":
            numero_conta = len(contas) + 1
            conta = criar_conta(AGENCIA, numero_conta, usuarios)

            if conta:
                contas.append(conta)

        elif opcao == "lc":
            listar_contas(contas)

        elif opcao == "q":
            break

        else:
            print("Operação inválida, por favor selecione novamente a operação desejada.")

# Chama a função principal para iniciar o programa
main()
import textwrap

def formatar_valor(valor):
    # valor em reais
    return f"R$ {valor:.2f}"

def validar_valor(valor_input):
    # somente numeros positivos
    valor_input = valor_input.replace('.', '').replace(',', '.')
    if valor_input.isdigit() or (valor_input.replace('.', '', 1).isdigit() and valor_input.count('.') < 2):
        return float(valor_input)
    return None

def menu():
    menu = """\n
    ================ MENU ================
    [d]\tDepositar
    [s]\tSacar
    [e]\tExtrato
    [nc]\tNova conta
    [lc]\tListar contas
    [nu]\tNovo usuário
    [q]\tSair
    => """
    # dedent tira os espaços
    return input(textwrap.dedent(menu))

# a / indica que o que for passado será por posição
def depositar(saldo, valor, extrato, /):
    if valor is not None and valor > 0:
        saldo += valor
        extrato += f"Depósito:\tR$ {valor:.2f}\n"
        print("\n=== Depósito realizado com sucesso! ===")
    else:
        print("\n@@@ Operação falhou! O valor informado é inválido. @@@")

    return saldo, extrato

# passado por forma nomeada após o asterisco *
def sacar(*, saldo, valor, extrato, limite, numero_saques, limite_saques):
    excedeu_saldo = valor > saldo
    excedeu_limite = valor > limite
    excedeu_saques = numero_saques >= limite_saques

    if excedeu_saldo:
        print("\n@@@ Operação falhou! Você não tem saldo suficiente. @@@")
    elif excedeu_limite:
        print("\n@@@ Operação falhou! O valor do saque excede o limite. @@@")
    elif excedeu_saques:
        print("\n@@@ Operação falhou! Número máximo de saques excedido. @@@")
    elif valor is not None and valor > 0:
        saldo -= valor
        extrato += f"Saque:\t\tR$ {valor:.2f}\n"
        numero_saques += 1
        print("\n=== Saque realizado com sucesso! ===")
    else:
        print("\n@@@ Operação falhou! O valor informado é inválido. @@@")

    return saldo, extrato, numero_saques  # Retorna o número de saques atualizado

